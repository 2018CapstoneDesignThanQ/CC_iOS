import Foundation
import UIKit

extension UIColor {
    class var defaultBlue: UIColor {
        return #colorLiteral(red: 0.5803921569, green: 0.7647058824, blue: 0.8196078431, alpha: 1)
    }
    
    class var teal: UIColor {
        return #colorLiteral(red: 0.1607843137, green: 0.5058823529, blue: 0.5960784314, alpha: 1)
    }
    
    class var grayBlue: UIColor {
        return #colorLiteral(red: 0.3607843137, green: 0.5254901961, blue: 0.568627451, alpha: 1)
    }
    
    class var pointYellow: UIColor {
        return #colorLiteral(red: 0.9882352941, green: 0.8039215686, blue: 0.01176470588, alpha: 1)
    }
    
    class var textGray: UIColor {
        return #colorLiteral(red: 0.5019607843, green: 0.5019607843, blue: 0.5019607843, alpha: 1)
    }
}
